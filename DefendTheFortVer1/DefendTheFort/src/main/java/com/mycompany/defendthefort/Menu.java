    public void showGames(){
        int x = 1;
        for (GameScreen game : user.getGames()) {
            if (game!=null){
                
                JButton panel = new JButton();
                panel.setSize(130,30);
                JLabel label = new JLabel();
                label.setText(game.getName());               
                panel.add(label);
                label.setLocation(65, 15);
                panel.setBorder(BorderFactory.createLineBorder(Color.black));                
                pnlPartidas.add(panel);
                panel.setLocation(1, x*30);
                x++;
            }
        }   
    }
    
