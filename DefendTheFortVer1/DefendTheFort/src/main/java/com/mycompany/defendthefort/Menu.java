    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String name = txfGameName.getText();
        System.out.println(name);
        for (GameScreen game : user.getGames()) {
            if (game!= null){
                System.out.println(game.getName());
                if (game.getName().equals(name)){                
                    game.setVisible(true);
                    return;
                }     
            }
        }
        
        JOptionPane.showMessageDialog(this,"No se encontró una partida con el nombre "+ name);
        
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnNewGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewGameActionPerformed
        boolean condition = false;
        String name = txfGameName.getText();
        for (GameScreen game : user.getGames()) {
            if (game!= null){
                if (game.getName().equals(name)){                
                    JOptionPane.showMessageDialog(this,"Este nombre ya existe");
                    return;
                }
                else{
                    condition = true;
                }
            }    
        }
        //no se puede agregar dentro del ciclo ->  "java.util.ConcurrentModificationException"
        if (condition){
            GameScreen newGame = new GameScreen(name);
            user.getGames().add(newGame);
            for (GameScreen object : user.getGames()) {
            }
            showGames();
            JOptionPane.showMessageDialog(this,"Nueva partida agregada");
            newGame.setVisible(true);
        }
    }//GEN-LAST:event_btnNewGameActionPerformed

